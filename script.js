/* Olympion v9 lesson playlist and completion system */
(function(){
  const lessonRoot=document.getElementById('lessonPlayer');
  if(!lessonRoot || typeof OLYMPION_VIDEOS==='undefined') return;
  const params=new URLSearchParams(location.search);
  const course=params.get('course')||'physics-mechanics';
  const requestedId=params.get('id');
  const courseMap={
    'physics-mechanics':{title:'Fizik • Mekanik Başlangıç Rotası',desc:'Newton yasaları, model kurma ve TÜBİTAK tarzı soru çözümleri.',filter:v=>v.branch==='fizik'},
    'chem-equilibrium':{title:'Kimya • Fizikokimya Rotası',desc:'Denge, tepkime yönü ve kimya olimpiyatı soru analizi.',filter:v=>v.branch==='kimya'},
    'math-strategy':{title:'Matematik • Problem Stratejisi',desc:'İspat sezgisi, problem parçalama ve olimpiyat düşünme sistemi.',filter:v=>v.branch==='fizik'||v.branch==='kimya'}
  };
  const cfg=courseMap[course]||courseMap['physics-mechanics'];
  let videos=OLYMPION_VIDEOS.filter(v=>cfg.filter(v)).sort((a,b)=>(a.order||0)-(b.order||0)||(a.priority||0)-(b.priority||0)).slice(0,8);
  if(!videos.length) videos=OLYMPION_VIDEOS.slice(0,6);
  let currentIndex=Math.max(0,videos.findIndex(v=>v.id===requestedId));
  if(currentIndex<0) currentIndex=0;
  const storeKey='olympion_completed_'+course;
  const completed=new Set(JSON.parse(localStorage.getItem(storeKey)||'[]'));
  const $=id=>document.getElementById(id);
  function label(b){return b==='fizik'?'Fizik':b==='kimya'?'Kimya':'Proje';}
  function save(){localStorage.setItem(storeKey,JSON.stringify([...completed]));}
  function progress(){return videos.length?Math.round(completed.size/videos.length*100):0;}
  function renderPlaylist(){
    const list=$('lessonPlaylist'), ring=$('lessonProgressRing');
    if(ring){const p=progress(); ring.style.setProperty('--p',p+'%'); ring.textContent=p+'%';}
    if(!list) return;
    list.innerHTML=videos.map((v,i)=>`<button class="lesson-item ${i===currentIndex?'active':''} ${completed.has(v.id)?'done':''} ${v.premium?'locked':''}" data-index="${i}" type="button"><span class="check">${completed.has(v.id)?'✓':(i===currentIndex?'▶':'○')}</span><span><span class="lesson-item-title">${v.title}</span><small>${label(v.branch)} • ${v.premium?'Plus':'Açık'} içerik</small></span><span class="duration">${v.duration||'—'}</span></button>`).join('');
    list.querySelectorAll('.lesson-item').forEach(btn=>btn.addEventListener('click',()=>{const idx=Number(btn.dataset.index); if(videos[idx]?.premium){document.querySelector('.premium-trigger')?.click(); return;} loadVideo(idx);}));
  }
  let ytPlayer=null;
  function loadVideo(index){
    currentIndex=Math.max(0,Math.min(index,videos.length-1)); const v=videos[currentIndex];
    if(!v) return;
    $('lessonTitle') && ($('lessonTitle').textContent=v.title);
    $('lessonBranch') && ($('lessonBranch').textContent=label(v.branch)+' • '+(v.duration||'—'));
    $('lessonCourseTitle') && ($('lessonCourseTitle').textContent=cfg.title);
    $('lessonCourseDesc') && ($('lessonCourseDesc').textContent=cfg.desc);
    if(window.YT && YT.Player){
      if(ytPlayer && ytPlayer.loadVideoById){ytPlayer.loadVideoById(v.id);} else {ytPlayer=new YT.Player('lessonPlayer',{videoId:v.id,playerVars:{rel:0,enablejsapi:1,modestbranding:1},events:{onStateChange:function(e){if(e.data===YT.PlayerState.ENDED){markDone(true);}}}});}
    } else {
      lessonRoot.innerHTML=`<iframe src="https://www.youtube.com/embed/${v.id}?rel=0&enablejsapi=1" title="${v.title}" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture" allowfullscreen></iframe>`;
    }
    renderPlaylist();
    history.replaceState(null,'',`lesson.html?course=${encodeURIComponent(course)}&id=${encodeURIComponent(v.id)}`);
  }
  function markDone(autoNext=false){const v=videos[currentIndex]; if(!v) return; completed.add(v.id); save(); renderPlaylist(); if(autoNext && currentIndex<videos.length-1){setTimeout(()=>loadVideo(currentIndex+1),700);} }
  $('lessonDoneBtn')?.addEventListener('click',()=>markDone(false));
  $('lessonNextBtn')?.addEventListener('click',()=>{markDone(false); if(currentIndex<videos.length-1) loadVideo(currentIndex+1);});
  window.onYouTubeIframeAPIReady=function(){loadVideo(currentIndex);};
  if(window.YT && window.YT.Player) loadVideo(currentIndex); else loadVideo(currentIndex);
})();
